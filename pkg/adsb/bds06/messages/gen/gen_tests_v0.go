package main

import (
	"log"
	"os"
	"text/template"
	"time"
)

func main() {
	generateFile("format_05_v0_test.go", "Format05V0", "0x28")
	generateFile("format_06_v0_test.go", "Format06V0", "0x30")
	generateFile("format_07_v0_test.go", "Format07V0", "0x38")
	generateFile("format_08_v0_test.go", "Format08V0", "0x40")
}

func generateFile(fileName string, name string, messageCode string) {
	// Open the target file
	f, err := os.Create(fileName)
	if err != nil {
		log.Fatal(err)
	}

	// Close at the end
	defer func() {
		closeErr := f.Close()
		if closeErr != nil {
			log.Fatal(err)
		}
	}()

	// Execute the template
	err = builderTemplate.Execute(
		f,
		struct {
			Timestamp   time.Time
			Name        string
			MessageCode string
		}{
			Timestamp:   time.Now(),
			Name:        name,
			MessageCode: messageCode,
		})
	if err != nil {
		log.Fatal(err)
	}
}

func die(err error) {
	if err != nil {
		log.Fatal(err)
	}
}

