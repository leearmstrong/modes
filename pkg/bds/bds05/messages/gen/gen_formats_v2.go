func generateFile(fileName string, name string, formatName string, isBaro bool) {
	// Open the target file
	f, err := os.Create(fileName)
	if err != nil {
		log.Fatal(err)
	}

	// Close at the end
	defer func() {
		closeErr := f.Close()
		if closeErr != nil {
			log.Fatal(err)
		}
	}()

	// Execute the template
	err = builderTemplate.Execute(
		f,
		struct {
			Timestamp  time.Time
			Name       string
			FormatName string
			IsBaro     bool
		}{
			Timestamp:  time.Now(),
			Name:       name,
			FormatName: formatName,
			IsBaro:     isBaro,
		})
	if err != nil {
		log.Fatal(err)
	}
}

func die(err error) {
	if err != nil {
		log.Fatal(err)
	}
}

