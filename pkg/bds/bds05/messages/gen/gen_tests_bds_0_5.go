// go generate

package main

import (
	"log"
	"os"
	"text/template"
	"time"
)

func main() {
	// Open the target file
	f, err := os.Create("bds_0_5_test.go")
	if err != nil {
		log.Fatal(err)
	}

	// Close at the end
	defer func() {
		closeErr := f.Close()
		if closeErr != nil {
			log.Fatal(err)
		}
	}()

	type FormatDefinition struct {
		Name        string
		MessageCode string
	}

	formatDefinitions := []FormatDefinition{
		{Name: "Format09", MessageCode: "0x48"},
		{Name: "Format10", MessageCode: "0x50"},
		{Name: "Format11", MessageCode: "0x58"},
		{Name: "Format12", MessageCode: "0x60"},
		{Name: "Format13", MessageCode: "0x68"},
		{Name: "Format14", MessageCode: "0x70"},
		{Name: "Format15", MessageCode: "0x78"},
		{Name: "Format16", MessageCode: "0x80"},
		{Name: "Format17", MessageCode: "0x88"},
		{Name: "Format18", MessageCode: "0x90"},
		{Name: "Format20", MessageCode: "0xA0"},
		{Name: "Format21", MessageCode: "0xA8"},
		{Name: "Format22", MessageCode: "0xB0"},
	}

	// Execute the template
	err = builderTemplate.Execute(
		f,
		struct {
			Timestamp         time.Time
			FormatDefinitions []FormatDefinition
		}{
			Timestamp:         time.Now(),
			FormatDefinitions: formatDefinitions,
		})
	if err != nil {
		log.Fatal(err)
	}
}

func die(err error) {
	if err != nil {
		log.Fatal(err)
	}
}

